function setLanguage(lang) {
  // Update all elements with data-i18n
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Update all placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  // Save choice and update HTML lang attribute
  localStorage.setItem("evoxLang", lang);
  document.documentElement.lang = lang;

  // --- NEW: Trigger project re-render if function exists ---
  if (
    typeof renderProjects === "function" &&
    typeof currentActiveFilter !== "undefined"
  ) {
    renderProjects(currentActiveFilter);
  }
}

// --- UPDATED LOGIC FOR CUSTOM SELECT ---

function initLanguageSwitcher() {
  const langSelect = document.getElementById("langCustomSelect");
  if (!langSelect) return; // Prevents the "null" error if element isn't found

  const trigger = langSelect.querySelector(".select-trigger");
  const options = langSelect.querySelectorAll(".option");
  const displayValue = document.getElementById("langSelectValue");

  // 1. Initial Load: Apply saved language and update the trigger text
  const savedLang = localStorage.getItem("evoxLang") || "en";
  setLanguage(savedLang);
  if (displayValue) {
    displayValue.textContent = savedLang.toUpperCase();
    const element = document.querySelector(
      `div[data-value="${savedLang.toLowerCase()}"] vox`,
    );
    displayValue.innerHTML = element.innerHTML;
  }

  // 2. Toggle Dropdown visibility
  trigger.addEventListener("click", (e) => {
    langSelect.classList.toggle("active");
    e.stopPropagation();
  });

  // 3. Handle Language Selection from the custom options
  options.forEach((option) => {
    option.addEventListener("click", () => {
      const lang = option.getAttribute("data-value");

      // Update UI display text
      if (displayValue) {
        displayValue.textContent = lang.toUpperCase();
        const element = document.querySelector(
          `div[data-value="${lang.toLowerCase()}"] vox`,
        );
        displayValue.innerHTML = element.innerHTML;
      }

      // Apply the actual translation logic
      setLanguage(lang);

      // Close dropdown
      langSelect.classList.remove("active");
    });
  });

  // 4. Close dropdown when clicking anywhere else on the page
  document.addEventListener("click", () => {
    langSelect.classList.remove("active");
  });
}

// Initialize when the DOM is ready
if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initLanguageSwitcher);
} else {
  initLanguageSwitcher();
}
